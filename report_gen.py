def generate_whatsapp_link(generator, prediction):
    """Generate a pre-filled WhatsApp message link for sharing the report."""
    risk_emoji = {
        'HEALTHY':   '🟢',
        'MODERATE':  '🟡',
        'HIGH RISK': '🟠',
        'CRITICAL':  '🔴',
    }.get(prediction.risk_level, '⚪')

    recs = json.loads(prediction.recommendations) if prediction.recommendations else []
    top_action = recs[0]['action'] if recs else 'Keep monitoring.'

    message = (
        f"🔧 *GenPredict NG — Generator Health Report*\n\n"
        f"*Generator:* {generator.name} ({generator.kva_rating} KVA {generator.fuel_type})\n"
        f"*Make/Model:* {generator.make or '—'} / {generator.model or '—'}\n"
        f"*Age:* {generator.age_years} year(s)\n\n"
        f"━━━━━━━━━━━━━━━━━━━━━\n"
        f"*Health Score:* {prediction.health_score:.0f}/100\n"
        f"*Risk Level:* {risk_emoji} {prediction.risk_level}\n"
        f"*Days to Service:* {prediction.days_to_service}\n\n"
        f"*Top Recommendation:*\n{top_action}\n\n"
        f"━━━━━━━━━━━━━━━━━━━━━\n"
        f"*Indicators:*\n"
        f"• Usage Intensity: {prediction.uii*100:.1f}% of daily max\n"
        f"• Fuel Efficiency: {int((prediction.fer-1)*100):+d}% vs expected\n"
        f"• Maintenance Due: {prediction.mos:.2f}x interval\n"
        f"• Load Stress: {prediction.aals:.3f}\n"
        f"• Fault Rate: {prediction.ffr:.3f}/day\n\n"
        f"_Report generated by GenPredict NG_"
    )

    encoded = urllib.parse.quote(message)
    return f"https://wa.me/?text={encoded}"
